#type casting converts the data type of a value to another data type

x = 1   #int
y = 2.0 #float
z = "3" #string

print(x)
print(y)
print(z) 

#change y to an integer
y = int(y)

print(y)

#print x as a string, for example if we wanted to display it along with another string. This will change x's data type for this specific print statement, but will not permanently change x's data type
print(str(x))
