#2D lists (multi-dimensional lists) are a list of lists

drinks = ["coffee", "soda", "tea"]
dinner = ["pizza", "hamburger", "hotdogs"]
dessert = ["cake", "ice cream"]

food = [drinks, dinner, dessert]

print(food)

print(food[1]) #you can use an index number to access a specific list in the 2d list

print(food[1][2]) #use this format to access a specific element within a specific list by their index position


